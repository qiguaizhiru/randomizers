async function main() {
  console.log('Building genshin_data.js...');

  const result = characters.map(c => {
    const [name, region, star, vision, element, weapon, special] = c;
    const enName = cnToEn[name.replace(/【.*】/, '')];
    let imgUrl = '';
    if (enName) {
      imgUrl = 'https://genshin.jmp.blue/characters/' + enName + '/icon-big.png';
    }
    return {
      name, img: imgUrl, region, star, vision, element, weapon, special
    };
  });

  // Filter out preview characters and duplicates
  const filtered = [];
  const seen = new Set();
  for (const c of result) {
    const cleanName = c.name.replace(/【.*】/, '').replace(/\s+$/, '');
    if (seen.has(cleanName)) continue;
    if (c.name.includes('【预告】') && !c.star) continue; // Skip unreleased
    seen.add(cleanName);
    filtered.push({ ...c, name: cleanName });
  }

  console.log('Total characters:', filtered.length);
  console.log('With images:', filtered.filter(c => c.img).length);

  const lines = filtered.map(c =>
    `  { name: ${JSON.stringify(c.name)}, img: ${JSON.stringify(c.img)}, region: ${JSON.stringify(c.region)}, star: ${JSON.stringify(c.star)}, vision: ${JSON.stringify(c.vision)}, element: ${JSON.stringify(c.element)}, weapon: ${JSON.stringify(c.weapon)}, special: ${JSON.stringify(c.special)} }`
  );
  const jsCode = 'const genshinDatabase = [\n' + lines.join(',\n') + '\n];\n';

  fs.writeFileSync('genshin_data.js', jsCode, 'utf8');
  console.log('Written genshin_data.js:', jsCode.length, 'bytes');

  // Test a few image URLs
  for (const name of ['刻晴', '那维莱特', '玛薇卡']) {
    const c = filtered.find(x => x.name === name);
    if (c?.img) {
      try {
        await new Promise((resolve, reject) => {
          https.get(c.img, { headers: { 'User-Agent': 'Mozilla/5.0' } }, res => {
            console.log(name, 'image status:', res.statusCode);
            res.resume();
            resolve();
          }).on('error', reject);
        });
      } catch (e) {
        console.log(name, 'image error:', e.message);
      }
    }
  }
}

